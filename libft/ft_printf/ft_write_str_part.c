#include "ft_printf.h"

size_t	ft_write_str_part(char **s)
{
	size_t	i;

	i = 0;
	while ((*s)[i] != '%' && (*s)[i])
		i++;
	if (i)
		write(1, *s, i);
	*s += i;
	return (i);
}
